window.sucheErgebnisse = function () {
  var ergebnisse = document.querySelector("#ergebnisse");
  ergebnisse.innerHTML = "";
  var AnzahlderErgebnisse = document.querySelector("#Anzahl");
  var wertAusDemSuchFeld = document.querySelector(".suche").value;
  var wertAusDemJahreszeitenFeld = document.querySelector("select.Jahreszeiten")
    .value;
  var wertAusDemAktivitätenFeld = document.querySelector("select.Aktivität")
    .value;
  var wertAusDemMaterialFeld = document.querySelector("select.Material").value;
  var WertAusDemFarbenFeld = document.querySelector(".Farbe").value;

  var gefilterteOutfits = outfits.filter(function (outfit) {
    var farbeEntsprichtAuswahlOderNochNichtGewählt =
      outfit.farbe.includes(WertAusDemFarbenFeld) ||
      WertAusDemFarbenFeld === "";
    var jahreszeitEntsprichtAuswahlOderNochNichtGewählt =
      outfit.jahreszeit === wertAusDemJahreszeitenFeld ||
      wertAusDemJahreszeitenFeld === "";
    var aktivitätEntsprichtAuswahlOderNochNichtGewählt =
      outfit.aktivität === wertAusDemAktivitätenFeld ||
      wertAusDemAktivitätenFeld === "";
    var materialEntsprichtAuswahlOderNochNichtGewählt =
      outfit.material === wertAusDemMaterialFeld ||
      wertAusDemMaterialFeld === "";
    var beschreibungEntsprichtAuswahlOderNochNichtGewählt =
      outfit.suchbegriffe.includes(wertAusDemSuchFeld) ||
      wertAusDemSuchFeld === "";
    return (
      farbeEntsprichtAuswahlOderNochNichtGewählt &&
      aktivitätEntsprichtAuswahlOderNochNichtGewählt &&
      materialEntsprichtAuswahlOderNochNichtGewählt &&
      jahreszeitEntsprichtAuswahlOderNochNichtGewählt &&
      beschreibungEntsprichtAuswahlOderNochNichtGewählt
    );
  });

  AnzahlderErgebnisse.innerText = gefilterteOutfits.length;

  for (var index = 0; index < gefilterteOutfits.length; index++) {
    ergebnisse.innerHTML += `
    <div class="ergebnis">
      <img height="300px" width="300px" src="${gefilterteOutfits[index].bild}"></img>
      <p>${gefilterteOutfits[index].beschreibung}</p>
    </div>`;
  }
};

window.sucheErgebnisse();

window.wieIstDasWetter = function () {
  var stadt = document.querySelector(".Stadt").value;
  const url = `https://api.openweathermap.org/data/2.5/weather?q=${stadt}&appid=4d8fb5b93d4af21d66a2948710284366&units=metric`;
  if (stadt !== "") {
    fetch(url)
      .then((response) => response.json())
      .then((data) => {
        var temperatur = data.main.temp.toString().replace(".", ",");
        var wetter = data.weather[0].main;
        var icon = `http://openweathermap.org/img/wn/${data.weather[0].icon}@2x.png`;

        var TemperaturAnzeige = document.querySelector("#Temperatur");
        TemperaturAnzeige.innerText = temperatur + "°C";

        var iconAnzeige = document.querySelector(".Icon");
        iconAnzeige.innerHTML = `<img src="${icon}"></img>`;
      });
  } else {
    console.log("Stadt ist leer");
  }
};

window.switchVideo = function (selectElement) {
  var Video = document.querySelector("#myVideo");
  var source = document.querySelector("#videoSource");
  var jahreszeit = selectElement.value;
  Video.pause();
  source.src = `https://github.com/Interhyp-Praktikum/Team-1/blob/main/${jahreszeit}.mp4?raw=true`;
  Video.load();
  Video.play();
};
